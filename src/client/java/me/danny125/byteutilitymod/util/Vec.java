package me.danny125.byteutilitymod.util;

public class Vec {
    double x,y,z;
    public Vec(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public double getZ() {
        return z;
    }
}
